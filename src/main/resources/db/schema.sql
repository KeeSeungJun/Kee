      CATEGORY_ID                         INT AUTO_INCREMENT                      NOT NULL                              COMMENT '분류 ID'
    , KNOW_CODE                           VARCHAR(10)                             NOT NULL                              COMMENT 'KNOW 직업분류 코드 (5자리)'
    , CATEGORY_LEVEL                      TINYINT                                 NOT NULL                              COMMENT '분류 레벨 (1:대분류, 2:중분류, 3:세세분류)'
