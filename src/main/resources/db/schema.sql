-- 지원 관리 테이블 (사용자가 채용공고에 지원한 내역)
CREATE TABLE IF NOT EXISTS JOB_APPLICATION (
      APPL_NO                             INT AUTO_INCREMENT                      NOT NULL                              COMMENT '지원 번호'
    , JOB_NO                              INT                                     NOT NULL                              COMMENT '채용공고 번호'
    , USR_ID                              VARCHAR(50)                             NOT NULL                              COMMENT '지원자 아이디'
    , APPL_STATUS                         VARCHAR(20)                             NOT NULL DEFAULT 'PENDING'            COMMENT '지원 상태 (PENDING:심사대기, ACCEPTED:합격, REJECTED:불합격)'
    , ADMIN_COMMENT                       TEXT                                    NULL                                  COMMENT '관리자 코멘트 (면접일정 등)'
    , APPL_DATE                           TIMESTAMP                               NOT NULL DEFAULT CURRENT_TIMESTAMP    COMMENT '지원 일시'
    , REVIEW_DATE                         TIMESTAMP                               NULL                                  COMMENT '심사 일시'
    , REVIEW_ADMIN_ID                     VARCHAR(50)                             NULL                                  COMMENT '심사한 관리자 ID'
    
    , PRIMARY KEY (APPL_NO)
    , INDEX IDX_APPLICATION01 (JOB_NO)
    , INDEX IDX_APPLICATION02 (USR_ID)
    , INDEX IDX_APPLICATION03 (APPL_STATUS)
    , INDEX IDX_APPLICATION04 (APPL_DATE)
    , FOREIGN KEY FK_APPLICATION_JOB (JOB_NO) REFERENCES JOB_INFO(JOB_NO) ON DELETE CASCADE
    , FOREIGN KEY FK_APPLICATION_USER (USR_ID) REFERENCES USR_INFO(USR_ID) ON DELETE CASCADE
) AUTO_INCREMENT=1 COMMENT = '채용 지원 내역';

