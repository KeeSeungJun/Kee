    , INDEX IDX_USR_INFO02 (USR_GRP_ID)
    , INDEX IDX_USR_INFO97 (USE_AT)
    , INDEX IDX_USR_INFO98 (CREAT_DT)
    , INDEX IDX_USR_INFO99 (UPDT_DT)
    ) AUTO_INCREMENT=1000000001 COMMENT = '사용자 정보';

